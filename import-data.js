    log('', 'white');
    info('Resumo da importa√ß√£o:');
    log(`   - Dom√≠nios processados: ${Object.keys(domains).length}`, 'cyan');
    log(`   - Total de h√°bitos: ${Object.values(domains).reduce((acc, domain) => acc + (domain.habits?.length || 0), 0)}`, 'cyan');
    log(`   - Metas integradas: ${data.integrated_goals?.length || 0}`, 'cyan');
