// Fun√ß√£o para fazer requisi√ß√µes HTTP
async function makeRequest(method, endpoint, data = null) {
  try {
    const config = {
      method,
      url: `${API_BASE_URL}${endpoint}`,
      headers: {
        'Content-Type': 'application/json',
      },
      timeout: 10000, // 10 segundos
    };

    if (data) {
      config.data = data;
    }

    const response = await axios(config);
    return response.data;
  } catch (err) {
    if (err.response) {
      throw new Error(`HTTP ${err.response.status}: ${err.response.data?.error || err.response.statusText}`);
    } else if (err.request) {
      throw new Error(`Erro de conex√£o: ${err.message}`);
    } else {
      throw new Error(`Erro: ${err.message}`);
    }
  }
}

// Fun√ß√£o para criar h√°bitos
async function createHabits(habits, domain) {
  info(`Criando ${habits.length} h√°bitos para o dom√≠nio: ${domain}`);
  
  const results = [];
  for (const habit of habits) {
    try {
      const habitData = {
        name: habit.name,
        icon: habit.icon,
        color: habit.color,
        categoryId: 1, // Valor padr√£o
        description: habit.description,
        target: habit.target,
        timeOfDay: habit.timeOfDay,
        domain: domain
      };

      const result = await makeRequest('POST', '/routines/habits', habitData);
      if (result.success) {
        success(`H√°bito criado: ${habit.name}`);
        results.push(result.data);
      } else {
        error(`Falha ao criar h√°bito ${habit.name}: ${result.error}`);
      }
    } catch (err) {
      error(`Erro ao criar h√°bito ${habit.name}: ${err.message}`);
    }
  }
  
  return results;
}

